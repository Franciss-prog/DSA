package main

import "fmt"

func main() {
	// strings
	var a string = "string to tanga"
	fmt.Println(a)

	// integers
	var b int = 12
	fmt.Println(b)

	var c float64 = 2.5
	fmt.Println(c)

	// boolean
	var d = true
	fmt.Println(d)

	//	initialized variable
	var e int
	e = 2
	fmt.Println(e)

	// shorthand syntax
	f := "ge"
	fmt.Println(f)
}
